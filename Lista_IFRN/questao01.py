#1. A imobiliária Imóbilis vende apenas terrenos retangulares. Faça um algoritmo para ler as dimensões de um terreno e depois exibir a área do terreno.


comprimento = float(input('Informe o valor do comprimento do terreno: '))
largura = float(input('Informe o valor da largura do terreno: '))
print('A área do terreno é: {}'.format(comprimento*largura))